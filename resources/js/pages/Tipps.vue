                        in der Regel gemäss folgender Formel berechnet: Kohlenhydrate, 
                        verfügbar (g/100g) = 100 – Protein (g/100g) – Fett, total (g/100g) 
                        – Wasser (g/100g) – Asche (g/100g) – Nahrungsfasern (g/100g)</p>
                    <p>Die Kohlenhydrate werden in drei Gruppen unterteilt: Einfachzucker, Zweifachzucker und 
