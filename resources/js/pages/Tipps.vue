                    <p>Kohlenhydrate sind die wichtigste Energiequelle für den menschlichen Körper. 
                        Sie liefern Energie und sind für die Funktion des Gehirns und des Nervensystems 
                        unerlässlich. Kohlenhydrate sind in Lebensmitteln wie Brot, Reis, Nudeln, Kartoffeln, 
